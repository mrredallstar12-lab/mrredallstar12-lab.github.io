function clampPopupPosition(element,preferredLeft,preferredTop,className=element.className,debug=false){
  const pad = 12;
  const viewport = getVisualViewportBox();
  let rect = element.getBoundingClientRect();
  let layoutWidth = element.offsetWidth || rect.width;
  let layoutHeight = element.offsetHeight || rect.height;
  if(layoutHeight < 100){
    element.style.minHeight = "190px";
    console.warn("[Odd Frequency] ad measured unusually short; forcing popup min-height", {className,height:layoutHeight});
    rect = element.getBoundingClientRect();
    layoutWidth = element.offsetWidth || rect.width;
    layoutHeight = element.offsetHeight || rect.height;
  }
  const width = Math.min(layoutWidth,Math.max(1,viewport.width - pad * 2));
  const height = Math.min(layoutHeight,Math.max(1,viewport.height - pad * 2));
  const minLeft = viewport.offsetLeft + pad;
  const minTop = viewport.offsetTop + pad;
  const maxLeft = Math.max(minLeft,viewport.offsetLeft + viewport.width - width - pad);
  const maxTop = rect.height >= viewport.height - pad * 2 ? minTop : Math.max(minTop,viewport.offsetTop + viewport.height - height - pad);
  const left = Math.round(Math.min(maxLeft,Math.max(minLeft,preferredLeft)));
  const top = Math.round(Math.min(maxTop,Math.max(minTop,preferredTop)));
  element.style.left = left + "px";
  element.style.top = top + "px";
  element.style.visibility = "visible";
  element.style.opacity = "1";
  if(debug) console.debug("[Odd Frequency] ad positioned", {className,width:layoutWidth,height:layoutHeight,left,top});
}


function makeDraggable(el,handle){
  if(!handle) return;
  let ox = 0;
  let oy = 0;
  let down = false;
  handle.onmousedown = (e) => {
    down = true;
    ox = e.clientX - el.offsetLeft;
    oy = e.clientY - el.offsetTop;
    e.preventDefault();
  };
  addEventListener("mousemove",(e)=>{
    if(!down) return;
    const left = e.clientX - ox;
    const top = e.clientY - oy;
    if(el.classList.contains("popup-ad")) clampPopupPosition(el,left,top,el.className,false);
    else {
      const viewport = getVisualViewportBox();
      const pad = 12;
      el.style.left = Math.max(viewport.offsetLeft + pad,Math.min(viewport.offsetLeft + viewport.width - el.offsetWidth - pad,left)) + "px";
      el.style.top = Math.max(viewport.offsetTop + pad,Math.min(viewport.offsetTop + viewport.height - Math.min(el.offsetHeight,viewport.height - pad * 2) - pad,top)) + "px";
    }
  });
  addEventListener("mouseup",()=>down=false);
}

